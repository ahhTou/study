package U01_数组;

public class c4遍历 {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5, 6};
        // 数组遍历
        for (int x = 0; x < 6; x++) {
            System.out.println(arr[x]);
        }
        for (int x = 0; x < arr.length; x++) {
            System.out.println(arr[x]);
        }

    }
}
