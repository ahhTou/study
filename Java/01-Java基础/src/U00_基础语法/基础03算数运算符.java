package U00_基础语法;

public class 基础03算数运算符 {

    public static void main(String[] args) {

        int a = 6;
        int b = 4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b); //得当商
        System.out.println(a % b); //得到余数

        //想要得到小数必须有浮点数的参与
        System.out.println(6.0 / 4 );
    }
}
